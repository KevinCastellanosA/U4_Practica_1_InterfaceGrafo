    private void CrearCaminoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CrearCaminoMouseClicked
       String Buscarcamino = Camino.getText().trim();
        if (Buscarcamino.matches("^\\S[A-z0-9]?[,]?([A-z0-9][,])*[A-z0-9]?$")) {
            String cad = Camino.getText().trim().replaceAll(",", "");
            jTextArea1.setText(interfaces.BuscarCamino(cad.toCharArray()));
        }
    }//GEN-LAST:event_CrearCaminoMouseClicked


    private void MatrizActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MatrizActionPerformed
       boolean datos[][] = interfaces.Mat_Adyacencia();
        String cad = "   ";
        for (int i = 0; i < jComboBox1.getItemCount(); i++) {
            cad += jComboBox1.getItemAt(i) + " ";
        }
        for (int i = 0; i < jComboBox1.getItemCount(); i++) {
            cad += "\n" + jComboBox1.getItemAt(i) + " ";
            for (int j = 0; j < jComboBox1.getItemCount(); j++) {
                cad += datos[i][j] ? "1 " : "0 ";
            }
        }
        jTextArea2.setText(cad); 
    }//GEN-LAST:event_MatrizActionPerformed

    private void AgregarNodoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AgregarNodoMouseClicked
       char dato = jTextField1.getText().charAt(0);
        if (interfaces.InsertarNodo(dato)) {
            jComboBox1.addItem(dato);
            jComboBox2.addItem(dato);
            jComboBox3.addItem(dato);
            jTextArea1.setText("Se agrego correctamente el Nodo "+dato); 
        }
    }//GEN-LAST:event_AgregarNodoMouseClicked

    private void EliminarNodoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EliminarNodoMouseClicked
        char EN = (char) jComboBox1.getSelectedItem();
        if (interfaces.EliminarNodo(EN)) {
            jComboBox1.removeItem(EN);
            jComboBox2.removeItem(EN);
            jComboBox3.removeItem(EN);
        }
        jTextArea1.setText("Se elimino el Nodo correctamente");
    }//GEN-LAST:event_EliminarNodoMouseClicked

